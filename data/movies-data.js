    // Get movies for Friday/Saturday
    getWeekendMovies: function(dayNumber) {
        // Rotate through recommendations
        const startIndex = (dayNumber % 10) * 3;
        const recommendedMovies = this.movies.filter(m => m.recommended);
        
        const selected = [];
        for (let i = 0; i < 3; i++) {
            const index = (startIndex + i) % recommendedMovies.length;
            selected.push(recommendedMovies[index]);
        }
        
        return selected;
    },

    // Get random movie
    getRandomMovie: function() {
        const recommended = this.movies.filter(m => m.recommended);
        return recommended[Math.floor(Math.random() * recommended.length)];
    },

    // Search movies
    searchMovies: function(query) {
        const q = query.toLowerCase();
        return this.movies.filter(m => 
            m.title.toLowerCase().includes(q) ||
            m.director.toLowerCase().includes(q) ||
            m.genre.some(g => g.toLowerCase().includes(q))
        );
    },

    // Get by IMDb rating
    getTopRated: function(minRating = 7.5) {
        return this.movies
            .filter(m => m.imdb >= minRating)
            .sort((a, b) => b.imdb - a.imdb);
    }
